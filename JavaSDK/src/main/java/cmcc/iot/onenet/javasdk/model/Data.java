package cmcc.iot.onenet.javasdk.model;

public class Data{
	private String at;
	private Object  value;
	public Data(String at, Object value) {
		super();
		this.at = at;
		this.value = value;
	}
	public String getAt() {
		return at;
	}
	public void setAt(String at) {
		this.at = at;
	}
	public Object getValue() {
		return value;
	}
	public void setValue(Object value) {
		this.value = value;
	}
	
}