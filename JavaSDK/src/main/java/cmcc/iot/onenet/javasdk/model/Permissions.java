package cmcc.iot.onenet.javasdk.model;

import java.util.List;

public class Permissions {
	private List<Devices>resources;
	 private List<String>access_methods;

	public Permissions(List<Devices> resources, List<String> access_methods) {
		this.resources = resources;
		this.access_methods = access_methods;
	}
	public List<Devices> getResources() {
		return resources;
	}
	public void setResources(List<Devices> resources) {
		this.resources = resources;
	}
	public List<String> getAccess_methods() {
		return access_methods;
	}
	public void setAccess_methods(List<String> access_methods) {
		this.access_methods = access_methods;
	}
}


